#include "lem_in.h"

int			ft_nbroom(t_info *info)
{
	t_room	*tmp;
	int		i;

	i = 0;
	tmp = info->room;
	while (tmp)
	{
		i++;
		tmp = tmp->next;
	}
	return (i);
}
