#include "lem_in.h"

int			ft_lenpipe(t_info *info)
{
	int		i;
	t_pipe	*tmp;

	i = 0;
	tmp = info->pipe;
	while (tmp)
	{
		i++;
		tmp = tmp->next;
	}
	return (i);
}
